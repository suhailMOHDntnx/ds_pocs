
package suhailmohd;


public class GenAttributeList {

    public String attribute;
    public String value;

    /**
     * No args constructor for use in serialization
     * 
     */
    public GenAttributeList() {
    }

    /**
     * 
     * @param attribute
     * @param value
     */
    public GenAttributeList(String attribute, String value) {
        super();
        this.attribute = attribute;
        this.value = value;
    }

}
